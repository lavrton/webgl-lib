const RGB_REGEX = /rgb\((\d{1,3}),(\d{1,3}),(\d{1,3})\)/;

export function getRandomColor() {
  var randColor = ((Math.random() * 0xffffff) << 0).toString(16);
  while (randColor.length < 6) {
    randColor = "0" + randColor;
  }
  return "#" + randColor;
}

export function colorToRGBA(str: string) {
  str = str || "black";
  return (
    _namedColorToRBA(str) ||
    _hex3ColorToRGBA(str) ||
    _hex6ColorToRGBA(str) ||
    _rgbColorToRGBA(str) ||
    _rgbaColorToRGBA(str) ||
    _hslColorToRGBA(str)
  );
}
// Parse named css color. Like "green"
function _namedColorToRBA(str: string) {
  var c = NAMES_COLORS[str.toLowerCase()];
  if (!c) {
    return null;
  }
  return {
    r: c[0],
    g: c[1],
    b: c[2],
    a: 1
  };
}
// Parse rgb(n, n, n)
function _rgbColorToRGBA(str: string) {
  if (str.indexOf("rgb(") === 0) {
    str = str.match(/rgb\(([^)]+)\)/)[1];
    var parts = str.split(/ *, */).map(Number);
    return {
      r: parts[0],
      g: parts[1],
      b: parts[2],
      a: 1
    };
  }
}
// Parse rgba(n, n, n, n)
function _rgbaColorToRGBA(str: string) {
  if (str.indexOf("rgba(") === 0) {
    str = str.match(/rgba\(([^)]+)\)/)[1];
    var parts = str.split(/ *, */).map(Number);
    return {
      r: parts[0],
      g: parts[1],
      b: parts[2],
      a: parts[3]
    };
  }
}
// Parse #nnnnnn
function _hex6ColorToRGBA(str: string) {
  if (str[0] === "#" && str.length === 7) {
    return {
      r: parseInt(str.slice(1, 3), 16),
      g: parseInt(str.slice(3, 5), 16),
      b: parseInt(str.slice(5, 7), 16),
      a: 1
    };
  }
}
// Parse #nnn
function _hex3ColorToRGBA(str: string) {
  if (str[0] === "#" && str.length === 4) {
    return {
      r: parseInt(str[1] + str[1], 16),
      g: parseInt(str[2] + str[2], 16),
      b: parseInt(str[3] + str[3], 16),
      a: 1
    };
  }
}
// Code adapted from https://github.com/Qix-/color-convert/blob/master/conversions.js#L244
function _hslColorToRGBA(str: string) {
  // Check hsl() format
  if (/hsl\((\d+),\s*([\d.]+)%,\s*([\d.]+)%\)/g.test(str)) {
    // Extract h, s, l
    const [_, ...hsl] = /hsl\((\d+),\s*([\d.]+)%,\s*([\d.]+)%\)/g.exec(str);

    const h = Number(hsl[0]) / 360;
    const s = Number(hsl[1]) / 100;
    const l = Number(hsl[2]) / 100;

    let t2;
    let t3;
    let val;

    if (s === 0) {
      val = l * 255;
      return {
        r: Math.round(val),
        g: Math.round(val),
        b: Math.round(val),
        a: 1
      };
    }

    if (l < 0.5) {
      t2 = l * (1 + s);
    } else {
      t2 = l + s - l * s;
    }

    const t1 = 2 * l - t2;

    const rgb = [0, 0, 0];
    for (let i = 0; i < 3; i++) {
      t3 = h + (1 / 3) * -(i - 1);
      if (t3 < 0) {
        t3++;
      }

      if (t3 > 1) {
        t3--;
      }

      if (6 * t3 < 1) {
        val = t1 + (t2 - t1) * 6 * t3;
      } else if (2 * t3 < 1) {
        val = t2;
      } else if (3 * t3 < 2) {
        val = t1 + (t2 - t1) * (2 / 3 - t3) * 6;
      } else {
        val = t1;
      }

      rgb[i] = val * 255;
    }

    return {
      r: Math.round(rgb[0]),
      g: Math.round(rgb[1]),
      b: Math.round(rgb[2]),
      a: 1
    };
  }
}
